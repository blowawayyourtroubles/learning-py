studentScore = int(input("Score: "))

if studentScore >= 90:
     print(f"Your score {studentScore}: A")
elif studentScore >= 80:
     print(f"Your score {studentScore}: B")
elif studentScore >= 70:
     print(f"Your score {studentScore}: C")
elif studentScore >= 60:
     print(f"Your score {studentScore}: D")
else:
     print(f"Your score {studentScore}: F") 